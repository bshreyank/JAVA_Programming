package Array;

public class basicPrinting {
    public static void main(String[] args) {
        //Basic Printing =====================================================================================>
        int[] num = {10,20,30,40,50,45,12,13,15};
        
        for(int i=0;i<num.length;i++){
            System.out.print(num[i]+" ");
        }
        
    }//main
}//class
